#include "libft.h"

char	*ft_strcat_from(char *str1, char *str2, int i)
{
	int	j;

	j = 0;
	while (str1[j])
		j++;
	while (str2[i++])
	{
		str1[j] = str2[i];
		j++;
	}
	str1[j] = '\0';
	return (str1);
}
