#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1);
	s2 = (char *)malloc(len * sizeof(*s1) + 1);
	if (s2 == NULL)
		return ((char *)NULL);
	ft_strcpy(s2, s1);
	return (s2);
}
