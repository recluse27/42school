#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char	*str;
	int		i;

	i = 0;
	if (!(str = ft_strnew(n)))
		return (NULL);
	while (s[i] && i < n)
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
