int	ft_sqrt(int nb)
{
	int	res;
	int i;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	res = 1;
	i = 1;
	while (i <= nb / 2)
	{
		i++;
		if (i * i == nb)
			return (i);
	}
	return (0);
}
