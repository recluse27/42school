void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int nbp;

	if (nb < 0)
	{
		ft_putchar('-');
		nbp = nb * -1;
	}
	else
		nbp = nb;
	if (nbp >= 10)
	{
		ft_putnbr(nbp / 10);
	}
	ft_putchar(nbp % 10 + 48);
}
